public class Operators {

	public static void main(String args[]){
		
	int x = 10;
		System.out.println("x = "+ x);
		System.out.println("++x degeri = "+ ++x);
		System.out.println("x = "+ x);
	x = 10;
		System.out.println("x++ degeri = "+ x++);
		System.out.println("x = "+ x);
	x = 10;
	int a = x--;
		System.out.println("x‐‐ degeri = "+a);
		System.out.println("x = "+ x);
	x = 10;
		a = --x;
		System.out.println("‐‐x degeri = "+a);
		System.out.println("x = "+ x);
	}
	
} 